import mongoose from "mongoose";

const thorSchema = new mongoose.Schema({
    id: {
        type: Number,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    image_url: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    price: {
        type: String,
        required: true,
    }
},
    {
        collection: "thor"
    });

export const Thor = mongoose.model("thor", thorSchema);
